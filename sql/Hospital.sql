-- ----------------------------
-- Function structure for ASSIGN_EMPLOYEES
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "TFUSER3"."ASSIGN_EMPLOYEES" AS
BEGIN
	FOR R IN (SELECT EMPLOYEE_ID FROM EMPLOYEES ORDER BY HIRE_DATE)
	LOOP
		FOR C IN (SELECT CHOICE FROM OPTIONS WHERE EMPLOYEE_ID = R.EMPLOYEE_ID ORDER BY CHOICE)
		LOOP
			EMP_CHOICE := C.CHOICE;
			COUNT_PEOPLE := (SELECT COUNT(*) FROM EMPLOYEES WHERE DEPARTMENT_ID = EMP_CHOICE);
			AVAIL_SPACE := (SELECT LIMIT FROM LIMITS WHERE DEPARTMENT_ID = EMP_CHOICE);
			IF AVAIL_SPACE > 0 THEN
				UPDATE EMPLOYEES
				SET DEPARTMENT_ID = EMP_CHOICE
				WHERE EMPLOYEE_ID = R.EMPLOYEE_ID;
				
				UPDATE LIMITS 
				SET LIMIT = LIMIT - 1
				WHERE DEPARTMENT_ID = EMP_CHOICE;
				
				INSERT INTO EMPLOYEE_DEPARTMENT VALUES(R.EMPLOYEE_ID, EMP_CHOICE);
				COMMIT;
			END IF;
		END LOOP;
	END LOOP;
END;
